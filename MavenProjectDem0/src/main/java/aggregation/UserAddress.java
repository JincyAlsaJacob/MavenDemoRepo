package aggregation;

public class UserAddress {
	String housename;
	String street;
	String postoffice;
	int pincode;
	String district;
	String state;
	UserAddress(String housename, String street, String postoffice, int pincode, String district, String state)
	{
		this.housename=housename;
		this.street=street;
		this.postoffice=postoffice;
		this.pincode=pincode;
		this.district=district;
		this.state=state;
		
	}
	
	

}
